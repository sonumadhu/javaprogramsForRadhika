package javaBasicPrograms;

public class BubblesortBasicLogic {

	public static void main(String[] args) {
		// Meaning of bubble sort
		int a=10;
		int b=20;
		
		int temp;
		temp=a;//temp=10
		a=b;//a=20
		b=temp;//b=10
		
		System.out.println("A value--->   " +a);
		System.out.println("B value--->   " +b);		
	
		

	}

}
